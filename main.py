def on_on_overlap(sprite, otherSprite):
    global isShark, bounceBackWaitTime
    if game.runtime() - 500 >= bounceBackWaitTime:
        if game.runtime() - 500 < weaponAnimationTime:
            SharkHealthBar.value += 0 - 100 / SHARK_MAX_HP
            if SharkHealthBar.value == 0:
                shark.destroy(effects.disintegrate, 2000)
                isShark = False
            otherSprite.set_velocity(enemyBounceBack * Math.sin(Math.atan2(shark.y - Tristan.y, shark.x - Tristan.x) * 180 / 3.14),
                enemyBounceBack * Math.cos(Math.atan2(shark.y - Tristan.y, shark.x - Tristan.x) * 180 / 3.14))
            bounceBackWaitTime = game.runtime()
        else:
            tristanStatusBar.value += 0 - 100 / TRISTAN_MAX_HP
            if tristanStatusBar.value == 0:
                game.over(False, effects.smiles)
            bounceBackWaitTime = game.runtime()
sprites.on_overlap(SpriteKind.player, SpriteKind.enemy, on_on_overlap)

